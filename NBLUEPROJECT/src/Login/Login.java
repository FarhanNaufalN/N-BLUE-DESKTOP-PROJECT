    private void btn_logActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_logActionPerformed
        try {
            String email = Username.getText();
            String password = Password.getText();

            String sql = "Select * FROM tb_anggota WHERE email = '"+email+"' AND password = '"+password+"'";
            Connection vcon = (Connection)koneksi.configDB();
            Statement s = vcon.createStatement();
            ResultSet r = s.executeQuery(sql);

            if (r.next()) {
                JOptionPane.showMessageDialog(null, "Login Berhasil");
                Main halamanUtama = new Main(r.getString(5),r.getString(1));
                halamanUtama.setVisible(true);
                this.setVisible(false);
                Menu.organisasi.setText(r.getString(5));
                Menu.nama.setText(r.getString(2));
            } else {
                JOptionPane.showMessageDialog(null, "Email atau Password Salah");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btn_logActionPerformed

  
